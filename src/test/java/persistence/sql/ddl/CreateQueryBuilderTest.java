package persistence.sql.ddl;

import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;
import persistence.sql.ddl.query.CreateQueryBuilder;

public class CreateQueryBuilderTest {

    @Test
    @DisplayName("Should create a CREATE TABLE query")
    public void shouldCreateCreateTableQuery() {
        CreateQueryBuilder queryBuilder = new CreateQueryBuilder();

        // When
        String query = queryBuilder.build(Person.class);

        // Then
        System.out.println(query);
        String expectedQuery = "CREATE TABLE person (id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY, nick_name VARCHAR(255), old INTEGER, email VARCHAR(255) NOT NULL, PRIMARY KEY (id));";
        assert query.equals(expectedQuery);
    }
}
